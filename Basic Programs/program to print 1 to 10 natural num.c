/* Devolop a algorithm by using while loop print 1 to 1o natural
number */
#include<stdio.h>
int main() {
    //Loop counter variable declaration and initialization
    int n=1;
    //Loop condition
    while(n<=10){
        //body of looop
        printf("%d,", n);
        //update loop counter variable
        n++;
    }
    return 0;
}




























